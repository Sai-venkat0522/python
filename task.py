# This script demonstrates basic Python syntax, variable declarations, arithmetic operations, and type casting

# Declare variables of different data types
string_var = "Hello, Python!"  # String
integer_var = 10               # Integer
float_var = 3.14               # Float
boolean_var = True             # Boolean

# Print the variables and their types
print("String variable:", string_var)
print("Integer variable:", integer_var)
print("Float variable:", float_var)
print("Boolean variable:", boolean_var)

# Perform a basic arithmetic operation
sum_result = integer_var + float_var
print("\nResult of adding integer and float:", sum_result)

# Casting an integer to a string and displaying the result
casted_integer = str(integer_var)
print("\nCasted integer to string:", casted_integer)

